export interface Match {
  versus: string;
  estadio: string;
  isAway: boolean;
  id_escudoteca: string;
  id_prom: string;
  datetime: string;
  ficha_partido: string;
  ficha_rival: string;
  youtube?: string;
  result?: string;
}

export function getNextMatch(): Match | null {
  const now = new Date();

  const upcomingMatches = FIXTURE.filter((match) => {
    const matchTime = new Date(match.datetime);
    const matchEndTime = new Date(matchTime.getTime() + 105 * 60 * 1000);

    return (
      !match.result &&
      (matchTime > now || (now >= matchTime && now <= matchEndTime))
    );
  }).sort(
    (a, b) => new Date(a.datetime).getTime() - new Date(b.datetime).getTime()
  );

  return upcomingMatches.length > 0 ? upcomingMatches[0] : null;
}

export function getRecentMatches(limit = 5): Match[] {
  return FIXTURE.filter((match) => match.result)
    .sort(
      (a, b) => new Date(b.datetime).getTime() - new Date(a.datetime).getTime()
    )
    .slice(0, limit);
}

export function getMatches(): Match[] {
  return FIXTURE;
}
